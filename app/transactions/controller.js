exports.add = async (req, res) => {
	try {
		const savedTransaction = await repository.add(req.body);
		res.success(savedTransaction);
		//res.success(extractObject(savedConfig, ["key", "value"],));
	}
	catch (err) {
		res.mongooseError(err)
		//res.send( err );
	}
};

exports.edit = async (req, res) => {
	try {
		var id = req.params.id ? req.params.id : null;
		var data = req.body ? req.body : null;
		const editedData = await repository.editTransactions(id, data);
		res.success(editedData);
	}
	catch (err) {
		res.send(err);
	}
};

exports.all = async (req, res) => {
	try {
		var id = req.params.id ? req.params.id : null;
		const transaction = await repository.findTransactions(id);
		res.success(transaction);
	}
	catch (err) {		
		res.mongooseError(err);
	}
};

exports.delete = async (req, res) => {
	try {
		var id = req.params.id ? req.params.id : null;
		const deleteTransaction = await repository.deleteTransactions(id);		
		res.success(deleteTransaction);
	}
	catch (err) {
		console.log(err);
		res.send(err);
	}
};
